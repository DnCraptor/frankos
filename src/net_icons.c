/* Parsed icon caches — heap-allocated on first use to save BSS */
static uint8_t *_connect_16, *_connect_32;
static uint8_t *_net_noact_16;
static uint8_t *_net_tx_16;
static uint8_t *_net_rx_16;
static uint8_t *_net_tx_rx_16;
static bool _net_icons_parsed = false;

/* FreeRTOS heap allocator (available in FRANK OS) */
extern void *pvPortMalloc(unsigned int);

static void _parse_net_icons(void) {
    if (_net_icons_parsed) return;
    _net_icons_parsed = true;

    _connect_16  = (uint8_t *)pvPortMalloc(256);
    _connect_32  = (uint8_t *)pvPortMalloc(1024);
    _net_noact_16 = (uint8_t *)pvPortMalloc(256);
    _net_tx_16   = (uint8_t *)pvPortMalloc(256);
    _net_rx_16   = (uint8_t *)pvPortMalloc(256);
    _net_tx_rx_16 = (uint8_t *)pvPortMalloc(256);

    if (_connect_16)  ico_parse_16(ico_connect,    sizeof(ico_connect),    _connect_16);
    if (_connect_32)  ico_parse_32(ico_connect,    sizeof(ico_connect),    _connect_32);
    if (_net_noact_16) ico_parse_16(ico_net_noact,  sizeof(ico_net_noact),  _net_noact_16);
    if (_net_tx_16)   ico_parse_16(ico_net_tx,     sizeof(ico_net_tx),     _net_tx_16);
    if (_net_rx_16)   ico_parse_16(ico_net_rx,     sizeof(ico_net_rx),     _net_rx_16);
    if (_net_tx_rx_16) ico_parse_16(ico_net_tx_rx,  sizeof(ico_net_tx_rx),  _net_tx_rx_16);
}

const uint8_t *net_icon16_connect_get(void)  { _parse_net_icons(); return _connect_16; }
